package jUnitProject;

public class Calculator {
	
	public int add(int a, int b) {
		int r = a + b;
		return r;
	}
	
	public int subtract(int a, int b) {
		int r = a - b;
		return r;
	}
	
	public double devide(double a, double b) {
		double r = a / b;
		return r;
	}
	
	public int multiply(int a, int b) {
		int r = a * b;
		return r;
	}

}
